export function getPodTemplatePrintRect(template: PodTemplate) {
  const faces = template.scenes.flatMap((scene) => scene.faces);
  if (faces.length === 0) {
    return { x: 0, y: 0, width: template.width, height: template.height };
  }

  const minX = Math.min(...faces.map((face) => face.x));
  const minY = Math.min(...faces.map((face) => face.y));
  const maxX = Math.max(...faces.map((face) => face.x + face.width));
  const maxY = Math.max(...faces.map((face) => face.y + face.height));

  return {
    x: roundTemplateValue(minX),
    y: roundTemplateValue(minY),
    width: roundTemplateValue(maxX - minX),
    height: roundTemplateValue(maxY - minY)
  };
}

function roundTemplateValue(value: number) {
  return Math.round(value * 1_000_000) / 1_000_000;
}

